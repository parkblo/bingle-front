function convertToGeoJson(originData: any) {
  return {
    type: "FeatureCollection",
    features: originData.roads.map((road: any) => ({
      type: "Feature",
      geometry: {
        type: "MultiLineString",
        coordinates: [
          road
            .replace("MULTILINESTRING((", "")
            .replace("))", "")
            .split(",")
            .map((coord: any) => coord.split(" ").map(Number)),
        ],
      },
    })),
  };
}

